@Override
    public void updateViewFromModel() {
        if (loginModel != null && loginModel.getLogin() != null) {
            jFormattedTextFieldCodigo.setText("0");
            jFormattedTextFieldName.setText(loginModel.getName().toString());
            jFormattedTextFieldLogin.setText(loginModel.getLogin().toString());
            jPasswordFieldSenha.setText(loginModel.getSenha().toString());
        } else {
            jFormattedTextFieldCodigo.setText("0");
            jFormattedTextFieldName.setText(loginModel.getName().toString());
            jFormattedTextFieldLogin.setText(loginModel.getLogin().toString());
            jPasswordFieldSenha.setText(loginModel.getSenha().toString());
        }
    }

    private void finalizeLoginView() {
        updateObserversWasFinalized();
        dispose();
    }

    private void record() {
        loginControler.persist();
        finalizeLoginView();
    }

    private void createActionCancel() {
        jButtonCancel.addActionListener(new ActionListener() {

            @Override
            public void actionPerformed(ActionEvent e) {
                finalizeLoginView();
            }

        });
    }

    private void createActionRecord() {
        jButtonRecord.addActionListener(new ActionListener() {

            @Override
            public void actionPerformed(ActionEvent e) {
                record();
            }
        });
    }

    @Override
    public void createActions() {
        createActionRecord();
        createActionCancel();
    }

    @Override
    public void configure() {
        createActions();
        setVisible(true);
    }

    @Override
    public void register(ObserverJInternalFrame o) {
        observerJInternalFrames.add(o);
    }

    @Override
    public void updateObserversWasFinalized() {
        observerJInternalFrames.stream().forEach((observerJInternalFrame) -> {
            observerJInternalFrame.wasFinalized(this);
        });
    }

    @Override
    public void updateViews(String msg) {
        if (msg != null) {
            JOptionPane.showMessageDialog(this, msg);
            return;
        }
        updateViewFromModel();
    }

    @Override
    public void errorOcurred(String error) {
        if (error == null) {
            JOptionPane.showMessageDialog(this, "um erro desconhecido ocorreu");
        } else {
            JOptionPane.showMessageDialog(this, error);
        }
    }
    @Override
    public void updateModelFromViewValues() {
        loginModel.setName(jFormattedTextFieldName.getText());
        loginModel.setLogin(jFormattedTextFieldLogin.getText());
        loginModel.setSenha(jPasswordFieldSenha.getText());
    }

