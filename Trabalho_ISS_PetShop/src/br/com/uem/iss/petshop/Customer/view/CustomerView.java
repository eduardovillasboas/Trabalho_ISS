    @Override
    public void register(ObserverJInternalFrame o) {
        observerJInternalFrames.add(o);
    }

    @Override
    public void updateObserversWasFinalized() {
        for (ObserverJInternalFrame observerJInternalFrame : observerJInternalFrames) {
            observerJInternalFrame.wasFinalized(this);
        }
    }

    @Override
    public void createActions() {
        createActionRecord();
        createActionCancel();
        createActionAddAnimal();
        createActionRemoveAnimal();
    }

    @Override
    public void configure() {
        createActions();
        setVisible(true);
    }

    @Override
    public void updateViews(String msg) {
        if (msg != null) {
            JOptionPane.showMessageDialog(this, msg);
            return;
        }
        updateViewFromModel();
    }

    @Override
    public void errorOcurred(String error) {
        if (error != null) {
            JOptionPane.showMessageDialog(this, error);
        } else {
            JOptionPane.showMessageDialog(this, "um erro desconhecido ocorreu");
        }
    }

    private void createActionCancel() {
        jButtonCancelar.addActionListener(new ActionListener() {

            @Override
            public void actionPerformed(ActionEvent e) {
                finalizeCustomerView();
            }

        });
    }

    private void createActionRecord() {
        jButtonRecord.addActionListener(new ActionListener() {

            @Override
            public void actionPerformed(ActionEvent e) {
                actionRecord();
            }
        });
    }

    private void finalizeCustomerView() {
        updateObserversWasFinalized();
        dispose();
    }

    private void actionRecord() {
        DateUtil dateUtil = new DateUtil();
        if (!dateUtil.isValid(jFormattedTextFieldBirth.getText())){
            JOptionPane.showMessageDialog(this, "Data inválida!","Informação", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        
        if (JOptionPane.showConfirmDialog(this, "Confirma gravação?", "Mensage do sistema", JOptionPane.INFORMATION_MESSAGE) != 
            JOptionPane.YES_OPTION)
            return;

        if (customerControler.persist()) {
            finalizeCustomerView();
        }

    }

    @Override
    public void updateViewFromModel() {
        DateUtil dateUtil = new DateUtil();

        jTextFieldName.setText(customerModel.getName());
        jTextFieldLastName.setText(customerModel.getLastName());
        jFormattedTextFieldBirth.setText(dateUtil.toString(customerModel.getBirthDate()));
        jTextFieldRG.setText(customerModel.getRg());
        jFormattedTextFieldCPF.setText(customerModel.getCPF());
        jTextFieldAddress.setText(customerModel.getAddress());
        if (customerModel.getNumber() == 0)
            jTextFieldNumber.setText("");
        else 
            jTextFieldNumber.setText(Long.toString(customerModel.getNumber()));
        jTextFieldPhone.setText(customerModel.getPhone());
        refreshTableAnimals();
    }

    @Override
    public void updateModelFromViewValues() {
        DateUtil dateUtil = new DateUtil();
        customerModel.setName(jTextFieldName.getText());
        customerModel.setLastName(jTextFieldLastName.getText());
        customerModel.setBirth(dateUtil.toDate(jFormattedTextFieldBirth.getText()));
        customerModel.setRg(jTextFieldRG.getText());
        customerModel.setCpf(jFormattedTextFieldCPF.getText());
        customerModel.setAddress(jTextFieldAddress.getText());
        if (jTextFieldNumber.getText().isEmpty())
            customerModel.setNumber(0L);
        else
            customerModel.setNumber(new Long(jTextFieldNumber.getText()));
        
        customerModel.setPhone(jTextFieldNumber.getText());
    }

    private void createActionAddAnimal() {
        jButtonAddAnimal.addActionListener(new ActionListener() {

            @Override
            public void actionPerformed(ActionEvent e) {
                addAnimal();
            }
        });
    }

    private void createActionRemoveAnimal() {
        jButtonRemoveAnimal.addActionListener(new ActionListener() {

            @Override
            public void actionPerformed(ActionEvent e) {
                removeAnimal();
            }
        });
    }

    private void addAnimal() {
        customerControler.addAnimal();
        refreshTableAnimals();
    }

    private void removeAnimal() {
        if (jTableAnimals.getSelectedRow() == -1){
            JOptionPane.showMessageDialog(this, "Nenhum animal selecionado", "Informação", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        Animal animal = customerModel.getAnimal(jTableAnimals.getSelectedRow());
        customerControler.removeAnimal(animal);
        refreshTableAnimals();
    }

    public boolean confirm(String confirmaRemocaoAnimal) {
        return JOptionPane.OK_OPTION == JOptionPane.showConfirmDialog(this, confirmaRemocaoAnimal);
    }

    private void refreshTableAnimals(){
        jTableAnimals.setModel(customerModel.getCreateModel());
        jTableAnimals.revalidate();
        jTableAnimals.clearSelection();
    }
